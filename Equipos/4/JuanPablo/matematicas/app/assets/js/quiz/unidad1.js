$('input[name="u1p1"][type="button"]').on('click', function(event){
    let i1 = $('input[name="u1p1i1"]:checked').val();
    let i2 = $('input[name="u1p1i2"]:checked').val();
    if (i1 === "c" && i2 === "c"){
        alert('Felicidades tienes todos los incisos correctos.');
    }
    else{
        if (i1 === "i" && i2 === "i"){
            alert('Incisos incorrectos');
        }
        else{
            if (i1 === "i"){
                alert('Inciso 1 incorrecto');
            }
            if (i2 === "i"){
                alert('Inciso 2 incorrecto');
            }
        }
    }
    event.preventDefault();
});

