$('input[name="pregunta_1"][type="button"]').on('click', function(event){
    let respuesta = $('input[name="respuesta"][type="radio"]:checked').val();
    if (respuesta === "incorrect"){
        alert('Respuesta incorrecta');
    }
    event.preventDefault();
});

